# In[ ]:


for i in 'Hello World':     # string
    print(i)

print(i)

print("\n")
for i in [1,2,3,4,5]:       # list
    print(i)

print("\n")
for i in {6,7,8,9,10}:      # sets
    print(i)

print("\n")
for i in (11,12,13,14,15):  # tuple
    print(i)
 
print("\n")
for i in range(10):
    print(i)

print("\n")
print(i)

